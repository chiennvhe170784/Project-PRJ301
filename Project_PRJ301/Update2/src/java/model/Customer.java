package model;

/**
 *
 * @author chien
 */
public class Customer {
    int id;
    double amount;
    String username;
    String password;
    int role;

    public Customer() {
    }

    public Customer(int id, double amount, String username, String password, int role) {
        this.id = id;
        this.amount = amount;
        this.username = username;
        this.password = password;
        this.role = role;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getAmount() {
        return amount;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public int getRole() {
        return role;
    }

    public void setRole(int role) {
        this.role = role;
    }
    
   
    
}
